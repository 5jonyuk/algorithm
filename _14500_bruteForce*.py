import sys
input = sys.stdin.readline
n, m = map(int, input().split())

paper = [list(map(int, input().split())) for _ in range(n)]

tetromino = [
    # blue
    [(0, 0), (0, 1), (0, 2), (0, 3)],
    [(0, 0), (1, 0), (2, 0), (3, 0)],

    # yellow
    [(0, 0), (0, 1), (1, 0), (1, 1)],

    # orange
    [(0, 0), (1, 0), (2, 0), (2, 1)],
    [(0, 0), (1, 0), (2, 0), (2, -1)],
    [(0, 0), (0, 1), (0, 2), (1, 2)],
    [(0, 0), (0, 1), (0, 2), (-1, 2)],
    [(0, 0), (0, 1), (1, 1), (2, 1)],
    [(0, 0), (0, 1), (1, 0), (2, 0)],
    [(0, 0), (0, 1), (0, 2), (1, 0)],
    [(0, 0), (1, 0), (1, 1), (1, 2)],

    # green
    [(0, 0), (1, 0), (1, 1), (2, 1)],
    [(0, 0), (0, 1), (-1, 1), (-1, 2)],
    [(0, 0), (1, 0), (1, -1), (2, -1)],
    [(0, 0), (0, 1), (1, 1), (1, 2)],

    # pink
    [(0, 0), (1, 0), (1, 1), (2, 0)],
    [(0, 0), (0, 1), (0, 2), (-1, 1)],
    [(0, 0), (0, 1), (0, 2), (1, 1)],
    [(0, 0), (1, 0), (1, -1), (2, 0)]
]

result = 0
for i in range(n):
    for j in range(m):
        for shape in tetromino:
            total = 0
            for x, y in shape:
                if (0 <= i+x < n and 0 <= j+y < m):
                    total += paper[i+x][j+y]
            result = max(result, total)

print(result)
